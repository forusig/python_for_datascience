# Operasi aritmatika sederhana
a = 10
b = 5
hasil_penjumlahan = a + b        # Penjumlahan
hasil_pengurangan = a - b        # Pengurangan
hasil_perkalian = a * b          # Perkalian
hasil_pembagian = a / b          # Pembagian (hasil dalam float)
hasil_modulus = a % b            # Modulus (sisa bagi)

print(hasil_modulus)